function cleanArray($array){

	$newArray = "";

	foreach($array as $a => $in){
		if($in != ""){
			$newArray[$a] = $in;
		}
	}

	return $newArray;
}

function cutText($text,$length){

	if(strlen($text) > $length)
		return substr($text,0,$length)."...";
	else
		return $text;
}

